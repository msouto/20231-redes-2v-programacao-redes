#Operadores Bitwise
#Operadores que trabalham no nível de bits:
x = 1
x << 2     # deslocamento dois bits a esquerda

print(x | 2)      # “Or" lógico bit-a-bit

print(x & 1)       # “And" lógico bit-a-bit

print(y = 0b1000)   # 8 em decimal   
print(y >> 3)       # deslocamento de três bits a direita
